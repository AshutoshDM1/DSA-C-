// Assignment Operators
#include <iostream>
using namespace std;
int main ()
{
  cout << "Assignment Operator" << endl;
  int a = 10 ;
  cout << "Value of a is : " << a << endl;
  a += 50;
  cout << "Value of a is : " << a << endl;
  a -= 50;
  cout << "Value of a is : " << a << endl;
  a *= 5;
  cout << "Value of a is : " << a << endl;
  a /= 5;
  cout << "Value of a is : " << a << endl;

  return 0;
}